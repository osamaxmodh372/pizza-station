let currentLang = localStorage.getItem('lang') || 'en';

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'ar' : 'en';
    localStorage.setItem('lang', currentLang);
    // GA Event: Language switched
    gtag('event', 'language_switch', {
        event_category: 'Engagement',
        event_label: currentLang === 'ar' ? 'Switched to Arabic' : 'Switched to English'
    });
    applyLanguage(currentLang);
}

function applyLanguage(lang) {
    const t = translations[lang];
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-key]').forEach(el => {
        const key = el.getAttribute('data-key');
        if (t[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = t[key];
            } else {
                el.textContent = t[key];
            }
        }
    });

    const langBtn = document.getElementById('langBtn');
    if (langBtn) langBtn.textContent = lang === 'en' ? '🌐 العربية' : '🌐 English';

    const dateError = document.getElementById('dateError');
    if (dateError && dateError.getAttribute('data-error') === 'past_date') {
        dateError.textContent = lang === 'ar' ? '❌ لا يمكن الحجز في تاريخ منتهي' : '❌ Cannot book a past date';
    }
}

function showCategory(category, btn) {
    const buttons = document.querySelectorAll('.filter-btn');
    buttons.forEach(b => b.classList.remove('active'));
    btn.classList.add('active');
    // GA Event: Menu category clicked
    gtag('event', 'menu_filter', {
        event_category: 'Menu',
        event_label: category === 'pizza' ? 'Pizza & Sides' : 'Drinks & Desserts'
    });

    if (category === 'pizza') {
        document.getElementById('pizza-section').style.display = 'block';
        document.getElementById('sides-section').style.display = 'none';
    } else {
        document.getElementById('pizza-section').style.display = 'none';
        document.getElementById('sides-section').style.display = 'block';
    }
}

const reservationForm = document.getElementById('reservationForm');
if (reservationForm) {
    reservationForm.addEventListener('submit', function(e) {
        let valid = true;

        const name = document.getElementById('name').value.trim();
        if (name === '') {
            document.getElementById('nameError').textContent = currentLang === 'ar' ? 'الاسم مطلوب' : 'Name is required';
            valid = false;
        } else { document.getElementById('nameError').textContent = ''; }

        const phone = document.getElementById('phone').value.trim();
        if (phone === '' || !/^(05|5)\d{8}$/.test(phone)) {
            document.getElementById('phoneError').textContent = currentLang === 'ar' ? 'أدخل رقم جوال سعودي صحيح' : 'Enter a valid Saudi phone number';
            valid = false;
        } else { document.getElementById('phoneError').textContent = ''; }

        const day = document.getElementById('day').value;
        const month = document.getElementById('month').value;
        const year = document.getElementById('year').value;

        if (day === '' || month === '' || year === '') {
            document.getElementById('dateError').textContent = currentLang === 'ar' ? 'اختر التاريخ كامل' : 'Please select a complete date';
            document.getElementById('dateError').removeAttribute('data-error');
            valid = false;
        } else {
            const selectedDate = new Date(year, month - 1, day);
            const today = new Date();
            today.setHours(0, 0, 0, 0);
            if (selectedDate < today) {
                document.getElementById('dateError').setAttribute('data-error', 'past_date');
                document.getElementById('dateError').textContent = currentLang === 'ar' ? '❌ لا يمكن الحجز في تاريخ منتهي' : '❌ Cannot book a past date';
                valid = false;
            } else {
                document.getElementById('dateError').removeAttribute('data-error');
                document.getElementById('dateError').textContent = '';
            }
        }

        const guests = document.getElementById('guests').value;
        if (guests === '') {
            document.getElementById('guestsError').textContent = currentLang === 'ar' ? 'اختر عدد الأشخاص' : 'Please select number of guests';
            valid = false;
        } else { document.getElementById('guestsError').textContent = ''; }

        if (!valid) {
            e.preventDefault();
        } else {
            // GA Event: Reservation form submitted successfully
            gtag('event', 'form_submit', {
                event_category: 'Reservation',
                event_label: 'Table Booking',
                value: parseInt(document.getElementById('guests').value) || 1
            });
        }
    });
}

const contactForm = document.getElementById('contactForm');
